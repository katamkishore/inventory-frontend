import { NavLink, useNavigate } from 'react-router-dom'
import { useAuth } from '../../context/AuthContext'
import logo from '../../assets/logo.png'
import styles from './Sidebar.module.css'

const navItems = [
  { to: '/home', label: 'Home', icon: <HomeIcon /> },
  { to: '/product', label: 'Product', icon: <ProductIcon /> },
  { to: '/invoice', label: 'Invoice', icon: <InvoiceIcon /> },
  { to: '/statistics', label: 'Statistics', icon: <StatisticsIcon /> },
  { to: '/setting', label: 'Setting', icon: <SettingIcon /> },
]

function Sidebar() {
  const { user, logout } = useAuth()
  const navigate = useNavigate()

  function handleLogout() {
    logout()
    navigate('/login')
  }

  return (
    <aside className={styles.sidebar}>
      <div className={styles.logo}>
        <img src={logo} alt="logo" className={styles.logoImg} />
      </div>
      <nav className={styles.nav}>
        {navItems.map(item => (
          <NavLink key={item.to} to={item.to}
            className={({ isActive }) => isActive ? `${styles.navItem} ${styles.active}` : styles.navItem}>
            <span className={styles.navIcon}>{item.icon}</span>
            <span>{item.label}</span>
          </NavLink>
        ))}
      </nav>
      <div className={styles.user} onClick={handleLogout} title="Click to logout" style={{ cursor: 'pointer' }}>
        <div className={styles.userAvatar} />
        <span className={styles.userName}>{user?.firstName || 'User'}</span>
      </div>
    </aside>
  )
}

